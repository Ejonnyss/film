def build():
    pack_shots = json.load(open(SHOTS_JSON, encoding="utf-8"))["shots"]
    start = {}
    t = 0.0
    for s in pack_shots:
        start[s["shot_id"]] = t
        t += s["duration_sec"]
    total = t

    GAP = 0.45          # минимальная пауза между репликами, с
    LEAD_IN = 0.35      # задержка после начала плана, с

    # реплики всегда идут в хронологии планов, даже если в списке порядок сбился
    order = {s["shot_id"]: i for i, s in enumerate(pack_shots)}
    ordered = sorted(LINES, key=lambda x: order[x[0]])

    lines = []
    prev_end = 0.0
    for i, (sid, speaker, text) in enumerate(ordered):
        if sid not in start:
            raise KeyError("Нет такого плана: %s" % sid)
        # оценка длительности речи: ~14.5 знаков в секунду для спокойного русского чтения
        est = max(1.2, len(text) / 14.5)
        anchor = start[sid] + LEAD_IN
        # реплика не начинается раньше, чем закончилась предыдущая + пауза
        t_start = max(anchor, prev_end + GAP)
        drift = t_start - anchor
        prev_end = t_start + est
        lines.append({
            "id": "VO_%03d" % (i + 1),
            "shot_id": sid,
            "speaker": speaker,
            "speaker_title": VOICES[speaker]["title"],
            "text": text,
            "start_sec": round(t_start, 2),
            "est_duration_sec": round(est, 2),
            "anchor_sec": round(anchor, 2),
            "drift_sec": round(drift, 2),
            "chars": len(text),
        })

    warns = []
    for l in lines:
        if l["drift_sec"] > 2.0:
            warns.append("%s (%s): сдвинута на %.1f c от своего плана — подрежьте текст выше"
                         % (l["id"], l["shot_id"], l["drift_sec"]))
    if lines and lines[-1]["start_sec"] + lines[-1]["est_duration_sec"] > total:
        warns.append("Речь выходит за хронометраж фильма — сократите реплики.")

    by_speaker = {}
    for l in lines:
        by_speaker[l["speaker"]] = by_speaker.get(l["speaker"], 0) + 1

    pack = {
        "project": "Пляж",
        "version": 2,
        "film_duration_sec": round(total, 1),
        "line_count": len(lines),
        "engines": {
            "primary": "edge-tts (нейронные голоса Microsoft, бесплатно, без ключа)",
            "offline": "silero (локально, без интернета)",
            "cloning": "XTTS v2 (клонирование — можно озвучить героев своими голосами)",
        },
        "voices": VOICES,
        "mix_targets": {"loudness_lufs": -14.0, "true_peak_dbfs": -1.0,
                        "voice_db": 0.0, "music_db": -11.0, "sea_db": -13.0},
        "lines": lines,
        "warnings": warns,
        "speaker_line_counts": by_speaker,
    }

    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(pack, f, ensure_ascii=False, indent=2)

    speech = sum(l["est_duration_sec"] for l in lines)
    print("OK ->", OUT)
    print("Реплик: %d | речи ~%.0f c из %.0f c фильма (%.0f%%)"
          % (len(lines), speech, total, 100 * speech / total))
    for k, v in by_speaker.items():
        print("  %-12s %2d реплик" % (VOICES[k]["title"], v))
    print("Предупреждений о наложении:", len(warns))
    for w in warns:
        print("  ", w)


if __name__ == "__main__":
    build()
